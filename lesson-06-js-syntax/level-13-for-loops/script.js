// Loop 1: 0 to 4 (exclusive)
for (let i = 0; i < 5; i++) {
  console.log("Loop 1:", i);
}

// Loop 2: 1 to 5 (inclusive)
for (let i = 1; i <= 5; i++) {
  console.log("Loop 2:", i);
}

// Loop 3: 10 down to 0 (inclusive)
for (let i = 10; i >= 0; i--) {
  console.log("Loop 3:", i);
}

// Loop 4: 2 to 8 (inclusive)
for (let i = 2; i <= 8; i++) {
  console.log("Loop 4:", i);
}

// Loop 5: 10 to 20 (exclusive)
for (let i = 10; i < 20; i++) {
  console.log("Loop 5:", i);
}

// Loop 6: 5 to 1 (downwards, inclusive)
for (let i = 5; i >= 1; i--) {
  console.log("Loop 6:", i);
}

// Loop 7: 0 to 10, stepping by 2
for (let i = 0; i <= 10; i += 2) {
  console.log("Loop 7:", i);
}

// Loop 8: -3 to 3 (inclusive)
for (let i = -3; i <= 3; i++) {
  console.log("Loop 8:", i);
}

// Loop 9: 100 to 90 (downwards, inclusive)
for (let i = 100; i >= 90; i--) {
  console.log("Loop 9:", i);
}

